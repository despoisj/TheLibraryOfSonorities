<h3 id="fade" class="elementExampleTitle">The Fade</h3>
<p>What I call the 'Fade' ending is a device present in many romantic pieces. It features a repeated pattern that fades off, often becoming softer and slower, before either stopping or lightly concluding. Sometimes it is simply block chords that are repeated a couple of times.<p>

<?php tt("Chopin - Nocturnes")?>
<p>At 9:34 Nocturne Op. 9 n°2: Repeated pattern. At 1:24:30 Nocturne Op. 55 n°1: Fading arps then fading final chords.</p>
