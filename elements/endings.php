<h3 id="finalfall" class="elementExampleTitle">The Final Fall</h3>
<p>The 'Final Fall' (Gjerdingen) is a typical ending where the melody jumps from a high ^1 to a lower ^1 (i.e. ^8 -> ^1), or from ^3 -> ^1 over a *1 bass, which may also drop lower. It is extremely common. In romantic music, it is often combined with the 'Fade' ending.</p>
<p>According to Gjerdingen, the ^3 -> ^1 fall might be more Romantic.</p>

<?php tt("John Field - Nocturne n°1")?>
<p>Medium G (^3) -> High Eb (^1) x2</p>
<p>Note: most of Field's nocturnes end with a final fall.</p>
<?php yt("aXwA4JkTiFM", 306) ?>

<?php tt("Mozart - 'Eine kleine Nachtmusik'")?>
<p>Medium G (^1) -> High B (^3) -> Low G (^1).</p>
<?php yt("MeaQ595tzxQ", 1024) ?>

<?php tt("Rachmaninoff - Prelude Op. 23 n°5")?>
<p>High G (^1) -> Low G (^1).</p>
<p>Note: although some recordings (including one by Rachmaninoff himself) change this ending for a more bombastic one, I love the minimalist simplicity and humour of a 1 or 2 soft notes ending to conclude a very intense piece. I'm looking for more examples of light/fun endings.</p>
