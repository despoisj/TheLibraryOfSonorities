<p>The <a href="https://en.wikipedia.org/wiki/Harmonic_major_scale">harmonic major</a> scale is a major scale with a lowered 6th degree. The resulting scale offers new chords, often seen as borrowed from the parallel minor scale. The main sonorities are those derived from the iiø7 chord, the V7b9 chord (shared with the harmonic minor), and the augmented bVI+ chord.</p>
