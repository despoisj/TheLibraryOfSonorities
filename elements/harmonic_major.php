<h4 class="elementExampleTitle">iiø7 Family</h4>
<p>The iiø7 family (for lack of a better term) regroups the ii°, iv, LT°7, iiø7 and their inversions. The most striking use is the full iiø7 chord, often in 65 inversion: ^4 b^6 ^1 ^2, which is the borrowed 6/5 sonority from the parallel minor. The telltale sign is the b^6 degree.</p>
