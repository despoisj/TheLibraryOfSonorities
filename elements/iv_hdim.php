<p>The ivø7 is another treasure of Romantic music that is never mentioned in books, and on which I'd like to shed light. This chord gives a very dark, brooding atmosphere. It is, in essence, the <a target="_blank" href="https://en.wikipedia.org/wiki/Tristan_chord">Tristan Chord</a>, although the real tristan chord resolves like an augmented 6th chord. In Russian musicology, it is called <a href="https://musescore.com/user/30712651/scores/7242678">Rachmaninoff's Subdominant</a>. It can be used as a predominant chord, or in a plagal motion. The inversions are also used, especially ivø65.</p>
<p>The sound is also close to the sound of the bvi chromatic mediant, as ivø7 = ^4 + bvi.  I will consider them as the same chord, as if the ivø7 was an extension except with a lower note. bvi seems to be closely linked to Schubert, with some alledgedly calling it Schubert's bvi. Interestingly, adding yet another note - ^2 - in the bass seems to work as well, to investigate more...</p>
<p>This sound is already very dark in minor, but can also be used in major for an even more clashing effect.</p>
<p>Note: most of the examples below come from <a target="_blank" href="https://musescore.com/user/30712651/scores/7242678">Сергей Щетников</a> or <a target="_blank" href="https://www.scsmt.org/2021_conf_files/bakulina_2021_slides.pdf">Ellen Bakulina</a>.</p>


