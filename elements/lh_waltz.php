<p>The Waltz style is characterized by a 3/4 time signature, "Bass + Chord + Chord" left hand pattern, often with an alternance of tonic and dominant chords. Note that it's one of the rare times where we find a I64 chord that isn't cadential.</p>
<p>Harmony often features repeated I -> I64 -> I ... or I6 -> I -> I6, as well as V65 -> V(7) -> ... blocks to get up/down motion in the bass (*3 -> *1, *1 -> *5, *7 -> *5 etc.), and similar structures on other harmonies.</p>

