<p>This section is about a topic that I find lacking in many improvisation guides and harmony manuals. Improvisation can rely on RO, sequences, counterpoint and so on, but sometimes one might end up on a chord without any idea of where to go next.</p>
<p>What I call harmonic or chord <i>"moves"</i> are options, or short sonorities, that can follow a specific chord. Knowing about the most common, and less common options is a great way to build your vocabulary, and reviewing the list below is a great exercise.</p>
<p>It is important to understand that these moves can originate from any chord and not necessarily a specific place within the scale (i.e. any major triad or major 63 and not just I or I6). This gives a lot of option by <i>reinterpretating</i> chords. For example, the RO move of *5 -> *4 involves a 642 chord, but you could also start it on *4 or *1, the latter giving a major 'Morte' sonority. For simplicity sake however, I might refer to a major triad as 'I' for example, but this is to be understood locally.</p>
<p>It's very subjective, but I've ranked the moves based on the intensity of their sonority, from low (◯◯⬤) to high (⬤⬤⬤)</p>

