<p>Nearly always in minor, the Neapolitan 6th chord (N6) is built with *4 in the bass harmonized with a b6/b3 chord (i.e degrees ^4 b^6 b^2). An easy way to use it is to simply lower the 6th when playing a chord of the 6th on *4. It often goes to i64 or V(7).</p>

