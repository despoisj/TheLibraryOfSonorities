<h3 id="ivvi" class="elementExampleTitle">I-V-V-I Schemas</h3>

<p>Many openers are based on a mirror archetype using a tonic - dominant - dominant - tonic pattern: I-V-(V)-I, with the third stage sometimes optional. Meyer calls this the <i>'Changing Note Archetype'</i>.</p>

