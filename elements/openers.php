<h3 id="pageone" class="elementExampleTitle">The 'Page One' or 'Lully'</h3>

<p>The 'Page One' (Mortensen) or 'Lully' (Rice) is a very famous progression composed of 4 stages: *1 -> *1 -> *7 -> *5 supporting harmony of 5/3 -> 6/4/2 -> 6/5 -> 5/3, either in major or minor.</p>


