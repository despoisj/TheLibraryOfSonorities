<h3 id="triadic" class="elementExampleTitle">Triadic Ascent & Fall</h3>

<?php tt("The Triadic Ascent") ?>
<p>"The Triadic Ascent most commonly features an idea built on scale degree ^1 followed by the same idea on scale degree ^3. Canonic imitation is typical, with the two canonic parts in parallel thirds.</p>

<p>It sometimes unfolds over a tonic pedal, and sometimes over a cadential progression such as the Romanesca (as in Monteverdi's "Zefiro torna") and I-IV-V-I (as in "Dove sono" in Mozart's Figaro).</p>

<p>The Do-Re-Mi often serves as the material repeated in a Triadic Ascent. In such cases the Triadic Ascent extends the Do-Re-Mi into a longer scalar ascent (Do-Re-Mi repeated up a third becomes Mi-Fa-Sol)." ~ John Rice</p>

