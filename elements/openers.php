<h3 id="minormajor" class="elementExampleTitle">The Minor-Major</h3>

<p>"What I call the Minor-Major is an opening gambit that eighteenth-century composers liked to use when working in the minor mode. A statement in the minor is repeated, a third higher (or occasionally a sixth lower), in the relative major." ~ John Rice</p>


