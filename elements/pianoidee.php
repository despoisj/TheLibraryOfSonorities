<p>The pianoidée (piano: soft, idée: idea) is a rather unknown term coined by Walther Krüger to represent a short phrase, often played softly, in the parallel minor of a major piece or passage. It has been extensively used by Vivaldi and Corelli.</p>
<p>Note: thanks to Leslie Sanford for the discovery of this technique!</p>

