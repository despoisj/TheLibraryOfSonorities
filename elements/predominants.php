<h3 id="c" class="elementExampleTitle">iv & iiø7</h3>
<div style="margin-top: -10px; margin-bottom: 25px;"><small>*M* & *m*</small></div>
<p>This is the equivalent of the iim7 and iim65 but for a minor mode resolution. Likewise, the outer voice wedge sounds great: iiø65 = (*4 b^6 ^8 ^2) -> (b*3 b^6 ^8 b^3) -> iiø7 = (*2 b^6 ^8 ^4).</p>
<p>One important use of the iiø65 chord is borrowed in major to give a <a href="index.php?filter=element&element=harmonic_major">harmonic major</a> sound. This is also a possibility for the predominant extension.</p>
<p><b>PREDOMINANT EXTENSION:</b> when using IV or iiø65 on top of V7, you get a V7 chord with a b9th and an 11th. On top of a simple *5, the third is missing.</p>

<p>Note: iim65, iim65 / *5, iiø65, and iiø65 / *5 can resolve to V7 or directly to i or I in a plagal cadence. See <a href="https://musescore.com/user/30712651/scores/7185111" target="_blank">this</a>.</p>

