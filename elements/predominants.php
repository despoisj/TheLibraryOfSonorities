<?php tt("Grieg - Lyric Pieces Book X, Op.71 n°2 'Summer Evening'") ?>
<p>mm. 1-2: I -> IVM7</p>
<?php yt("lXt3a13UEaU") ?>

<?php tt("Ravel - Pavane Pour une Infante Défunte") ?>
<p>mm. 1: I -> IVM7</p>
<?php yt("kBS8e_GMkFM", 294) ?>

<br>
<p><b>PREDOMINANT EXTENSION:</b> when using IVM7 on top of V7 or *5, you get an incredible V7sus chord with a 9th and a 13th. The suspension, ^1, often resolves down to ^7 to form a VChM9 chord, which can resolve to V7 or directly move to I. This can be combined with the iim7 extension to form an even richer iim9 extension sound, even though the 5th of the dominant chord isn't that important.</p>
<p>A common resolution for this chord is to resolve the suspension first to get a VChM9 chord, which can then resolve to V7 or directly to I. It is like the 4-3 suspension we find in compound cadences, or you can see it as the 2-5-1 <i>"box voicing"</i> in jazz. In C this voicing would be: Dmin9 (aka FMaj7/D), G7(9,13), C. If you had G in the bass instead of D, Fmaj7/G would be our Sus4(9,13) chord.</p>

