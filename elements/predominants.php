<h3 id="d" class="elementExampleTitle">IVM7 & iim9</h3>
<div style="margin-top: -10px; margin-bottom: 25px;"><small>*M*</small></div>
<p>In itself, using IVM7 instead of ii, IV, iim7 or iim65 gives a very sweet rich sound. For this, the 7th is often prepared.</p>

