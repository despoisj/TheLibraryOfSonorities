<h3 id="pre" class="elementExampleTitle">Predominants & Predominant Extensions</h3>

<p>In this section, we will explore some <a target="_blank" href="https://en.wikipedia.org/wiki/Predominant_chord#:~:text=In%20music%20theory%2C%20a%20predominant,and%20secondary%20leading%20tone%20chord.">predominant chords</a> that can be found in Romantic harmony.</p>
<p>In particular, we will focus on what I call <i>predominant extensions</i>: extended dominant harmonies that can be easily constructed like the <a target="_blank" href="https://en.wikipedia.org/wiki/Upper_structure">upper structure triads</a> found in jazz. The concept is simple: add a specific chord on top of a V7 chord (or simply *5 bass note) to create a richer harmony. This concept is very rarely explored, and I hope you will find the selection below as useful and beautiful as I do.</p>
<p>The main advantage of building chords this way is that it allows you to move the voices quickly: it's not that easy to quickly play and move voices of a V7b9 chord, but it's easy to move a vii°7 on top of a *5 bassline. Also note that when a predominant can be built with *4 in the bass, this allows the bass to move up to *5 without changing the upper harmony, like a suspension. Here's some of the most interesting combinations:</p>

