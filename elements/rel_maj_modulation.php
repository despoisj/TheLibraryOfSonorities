<p>This modulation moves from i to III, or from any minor chord to the Major scale a Major third above (ex. amin -> CMaj). The bass moves from *1 -> b*7 -> b*3 supporting harmonies of I -> V/III (aka VII) -> III. It's a good way to temporarily brighten a minor piece. It gives its very characteristic sound to the beginning of the Folia pattern.</p>

