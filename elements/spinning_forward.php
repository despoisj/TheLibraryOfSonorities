<p>This technique, that I leared from Nicola Canzano's video (see below), is inspired by the German concept of <i>"Fortspinnung"</i>, and consists in establishing a motive (<b>A</b>), creating a variation on the motive (<b>Ax</b>), and then spin off (or spin forward) a new idea based on the variation (<b>x</b>). You can then repeat the process again and again: A -> Ax -> x -> xy -> Y -> etc.</p>

