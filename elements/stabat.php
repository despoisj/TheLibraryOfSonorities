<p>The Stabat Mater Prinner is the name given to a specific chain of 2-3 suspension over a dominant pedal point in minor, in reference to a passage in Pergolesi's Stabat Mater. The slow descent gives a very sad and tragic sound.</p>
<p>For more information, check out <a target="_blank" href="https://essaysonmusic.com/variants-of-the-prinner-part-4-the-stabat-mater-prinner/">this great article</a> by Ewald Demeyere</p>

<p><b>Keyboard Tip:</b> over the dominant pedal point, the right hand starts as a tied bass motion, then moves like a cascade (D3U1 sequence).
</p>


