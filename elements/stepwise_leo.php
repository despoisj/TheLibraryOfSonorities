<p>The Stepwise romanesca, also called descending 5-6 sequence, descends diatonically from *1 and takes alternatively 5/3 and 6 chords. In major, it has a very light, happy and triumphant feel.</p>
<p>The Leo, mostly in major, is a variant that keeps tonic harmony in the melody, often as an ostinato, while the bass descends like the stepwise romanesca, either harmonized with alternating 5/3 and 6 chords or solely with the bass.</p>

